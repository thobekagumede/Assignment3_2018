
public class Tree {
	private int xCoord;
	private int yCoord;
	private int extent;
	
	public Tree(int xCoord, int yCoord, int extent) {
		super();
		this.xCoord = xCoord;
		this.yCoord = yCoord;
		this.extent = extent;
	}

	public int getxCoord() {
		return xCoord;
	}

	public void setxCoord(int xCoord) {
		this.xCoord = xCoord;
	}

	public int getyCoord() {
		return yCoord;
	}

	public void setyCoord(int yCoord) {
		this.yCoord = yCoord;
	}

	public int getExtent() {
		return extent;
	}

	public void setExtent(int extent) {
		this.extent = extent;
	}
	
	
	
}
